local Players = game:GetService("Players")
local LocalPlayer = Players.LocalPlayer
local RunService = game:GetService("RunService")
local UserInputService = game:GetService("UserInputService")

local Spectating = false
local isAlive = 1
local Gui = Converted._Spectate1

local Characters = function()
	local new = {}
	for _, player in pairs(Players:GetPlayers()) do
		if player and player.Character then
			table.insert(new, player.Character)
		end
	end
	return new
end

RunService.RenderStepped:Connect(function()
	if Gui.Visible == false then
		Spectating = false
	else
		Spectating = true
		local Active = Characters()
		if #Active == 0 then
			workspace.CurrentCamera.CameraSubject = nil
			LocalPlayer.CameraMode = Enum.CameraMode.Classic
			Gui.Player.Text = ""
			Gui.Frame.Left.Visible = false
			Gui.Frame.Right.Visible = false
		else
			if isAlive <= 0 then
				isAlive = #Active
			elseif #Active < isAlive then
				isAlive = isAlive - #Active
			end
			if Active[isAlive] == nil then
				isAlive = 1
			end
			local Name = Active[isAlive].Name
			if Players:FindFirstChild(Name) then
				Name = Players[Name].DisplayName
			end
			workspace.CurrentCamera.CameraType = Enum.CameraType.Custom
			workspace.CurrentCamera.CameraSubject = Active[isAlive]:FindFirstChildWhichIsA("Humanoid")
			LocalPlayer.CameraMode = Enum.CameraMode.Classic
			Gui.Player.Text = Name
			if #Active > 1 then
				Gui.Frame.Left.Visible = true
				Gui.Frame.Right.Visible = true
				return
			end
			Gui.Frame.Left.Visible = false
			Gui.Frame.Right.Visible = false
		end
	end
end)

Gui.Frame.Left.ImageButton.MouseButton1Click:Connect(function()
	isAlive = isAlive - 1
end)

Gui.Frame.Right.ImageButton.MouseButton1Click:Connect(function()
	isAlive = isAlive + 1
end)

UserInputService.InputBegan:Connect(function(input, gameProcessed)
	if not gameProcessed and Spectating then
		if input.KeyCode == Enum.KeyCode.Q then
			isAlive = isAlive - 1
		end
		if input.KeyCode == Enum.KeyCode.E then
			isAlive = isAlive + 1
		end
	end
end)

local toggle = function()
	if Gui.Visible == true then
		Gui.Visible = false
		workspace.CurrentCamera.CameraSubject = LocalPlayer.Character
		LocalPlayer.CameraMode = Enum.CameraMode.Classic
	else
		Gui.Visible = true
	end
end

Gui.Frame.Middle.ImageButton.MouseButton1Click:Connect(toggle)

return {
  PluginCreator = "toonarch",
	PluginName = "SpectateGui",
	PluginDescription = "Adds a Spectate Gui",
	Commands = {
		spectategui = {
			ListName = "spectategui",
			Description = "Opens the Spectate Gui",
			Aliases = {},
			Function = toggle
		}
	}
}
